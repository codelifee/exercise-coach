package com.shoppingmall.model;

public class DetailQualityTab {
	
	private int detailed_quality_id;
	private int product_detail_id;
	private String detailed_quality_picture;
	
	public int getDetailed_quality_id() {
		return detailed_quality_id;
	}
	public void setDetailed_quality_id(int detailed_quality_id) {
		this.detailed_quality_id = detailed_quality_id;
	}
	public int getProduct_detail_id() {
		return product_detail_id;
	}
	public void setProduct_detail_id(int product_detail_id) {
		this.product_detail_id = product_detail_id;
	}
	public String getDetailed_quality_picture() {
		return detailed_quality_picture;
	}
	public void setDetailed_quality_picture(String detailed_quality_picture) {
		this.detailed_quality_picture = detailed_quality_picture;
	}
	
	

}
